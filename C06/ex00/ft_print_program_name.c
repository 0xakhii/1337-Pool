#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	a;

	a = 0;
	argc = 0;
	while (argv[argc][a] != '\0')
	{
		write (1, &argv[argc][a], 1);
		a++;
	}
	write (1, "\n", 1);
	return (0);
}
